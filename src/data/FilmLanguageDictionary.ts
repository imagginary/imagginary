// Build lookup map for fast term resolution
const termMap = new Map<string, FilmTerm>();
FILM_DICTIONARY.forEach((entry) => {
  termMap.set(entry.term.toLowerCase(), entry);
  entry.aliases.forEach((alias) => termMap.set(alias.toLowerCase(), entry));
});

export function lookupFilmTerm(term: string): FilmTerm | undefined {
  return termMap.get(term.toLowerCase());
}

export function translateForPrompt(term: string): string {
  const entry = lookupFilmTerm(term);
  return entry ? entry.promptTranslation : term;
}

export function getDictionaryByCategory(category: FilmTerm['category']): FilmTerm[] {
  return FILM_DICTIONARY.filter((entry) => entry.category === category);
}

export default FILM_DICTIONARY;
